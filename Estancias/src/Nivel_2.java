    private void NumExpedienteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_NumExpedienteKeyTyped
        char c = evt.getKeyChar();
        if (!Character.isDigit(c) && c != evt.VK_BACK_SPACE) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_NumExpedienteKeyTyped

