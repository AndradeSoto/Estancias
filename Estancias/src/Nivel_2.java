    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        int cont=0;
        if (Masculino.isSelected()) {
            sexo = "Masculino";
        }else if (Femenino.isSelected()) {
            sexo = "Femenino";
        }
        InfoPasiente.add(NombrePasiente.getText());
        InfoPasiente.add(NumExpediente.getText());
        InfoPasiente.add(sexo);
        InfoPasiente.add(Alergias.getText());
        
        InfoPasiente.add((String)Area.getSelectedItem());
        InfoPasiente.add((String)TipoCama.getSelectedItem());
        InfoPasiente.add((String)Cama.getSelectedItem());
        InfoPasiente.add((String)RiesgoCaida.getSelectedItem());
        InfoPasiente.add((String)RiesgoUlsera.getSelectedItem());
        for (int i = 0; i < InfoPasiente.size(); i++) {
            String campo = ""+InfoPasiente.get(i);
            if (!campo.equals("")) {
                cont++;
                                     
            }
        }
        if (FechaNacimiento.getDate() != null) {
            cont++;
            
        }
        
        if (cont==10) {
            cont = 0;
            ConfirmarNombre.setText(NombrePasiente.getText());
            ConfirmarSexo.setText(sexo);
            ConfirmarExpediente.setText(NumExpediente.getText());
            ConfirmarArea.setText((String)Area.getSelectedItem()+" cama "+(String)TipoCama.getSelectedItem()+" numero "+(String)Cama.getSelectedItem());
            ConfirmarAlergias.setText(Alergias.getText());
            ConfirmarRiesgoCaida.setText((String)RiesgoCaida.getSelectedItem());
            ConfirmarRiesgoUlsera.setText((String)RiesgoUlsera.getSelectedItem());
            jFrame1.show();
            jFrame1.setLocationRelativeTo(null);
        }else{
            cont=0;
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos");
        }
    }//GEN-LAST:event_GuardarActionPerformed

